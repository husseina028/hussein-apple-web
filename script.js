let currentLang = 'ar';

function setLang(lang) {
  currentLang = lang;
  const t = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });
  // Handle hero title with highlight
  const titleEl = document.getElementById('hero-title');
  if (titleEl) {
    titleEl.innerHTML = `${t.hero_title_1}<span class="highlight">${t.hero_title_highlight}</span>${t.hero_title_2}`;
  }
  if (lang === 'en') {
    document.body.classList.add('en');
    document.body.setAttribute('dir', 'ltr');
  } else {
    document.body.classList.remove('en');
    document.body.setAttribute('dir', 'rtl');
  }
  localStorage.setItem('hussein-lang', lang);
}

function toggleLang() {
  setLang(currentLang === 'ar' ? 'en' : 'ar');
}

// ===== NAVBAR SCROLL =====
window.addEventListener('scroll', () => {
  document.querySelector('.navbar').classList.toggle('scrolled', window.scrollY > 50);
});

// ===== MOBILE MENU =====
function openMenu() { document.getElementById('mobileMenu').classList.add('open'); }
function closeMenu() { document.getElementById('mobileMenu').classList.remove('open'); }

// ===== SCROLL REVEAL =====
const observer = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
    }
  });
}, { threshold: 0.1 });

document.addEventListener('DOMContentLoaded', () => {
  // Init language
  const saved = localStorage.getItem('hussein-lang');
  setLang(saved || 'ar');
  
  // Observe reveal elements
  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));
  
  // Smooth scroll for nav links
  document.querySelectorAll('a[href^="#"]').forEach(a => {
    a.addEventListener('click', (e) => {
      e.preventDefault();
      const target = document.querySelector(a.getAttribute('href'));
      if (target) {
        target.scrollIntoView({ behavior: 'smooth' });
        closeMenu();
      }
    });
  });
});
